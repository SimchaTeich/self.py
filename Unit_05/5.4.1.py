def func(num1, num2):
    """
    calcs the sum of numbers.
    :param num1: number
    :type num1: integer
    :param num2: number
    :type num2: integer
    :return: the sum of the numbers.
    :rtype: integer
    """
    return num1 + num2

def main():
    print("7 + 3 = " + func(7, 3))

if __name__ == "__main__":
    main()
   